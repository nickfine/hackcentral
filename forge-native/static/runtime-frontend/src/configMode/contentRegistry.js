export const CONTENT_REGISTRY_BY_KEY = Object.freeze(
  CONTENT_REGISTRY.reduce((acc, field) => {
    acc[field.key] = field;
    return acc;
  }, {})
);

export const PHASE1_CONTENT_OVERRIDE_KEYS = new Set(
  CONTENT_REGISTRY.filter((field) => field.sourceKind === CONFIG_SOURCE_KINDS.CONTENT_OVERRIDE)
    .map((field) => field.key)
);

export function getRegistryField(key) {
  return CONTENT_REGISTRY_BY_KEY[key] || null;
}

export function getRegistryFieldsForPage(page) {
  return CONTENT_REGISTRY.filter((field) => field.page === page && field.editablePhase === 1);
}

export function validateRegistryFieldValue(field, value) {
  if (!field) return { valid: false, error: 'Unknown field' };

  if (field.fieldType === CONFIG_FIELD_TYPES.SELECT) {
    const optionValues = new Set((field.options || []).map((option) => option.value));
    const nextValue = value == null ? '' : String(value);
    if (!optionValues.has(nextValue)) {
      return { valid: false, error: `Invalid value for ${field.key}` };
    }
    return { valid: true };
  }

  const normalized = value == null ? '' : String(value);
  if (field.maxLength && normalized.length > field.maxLength) {
    return { valid: false, error: `${field.label || field.key} exceeds ${field.maxLength} characters` };
  }

  return { valid: true };
}
